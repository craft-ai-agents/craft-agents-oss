/**
 * Get sources filtered by category.
 */
export function getSourcesByCategory(category: GalleryCategory): GallerySource[] {
  if (category === 'all') return GALLERY_SOURCES
  return GALLERY_SOURCES.filter((s) => s.category === category)
}

/**
 * Get featured sources for hero section.
 */
export function getFeaturedSources(): GallerySource[] {
  return GALLERY_SOURCES.filter((s) => s.isFeatured)
}

/**
 * Get newly added sources.
 */
export function getNewSources(): GallerySource[] {
  return GALLERY_SOURCES.filter((s) => s.isNew)
}

/**
 * Search sources by name, provider, or description.
 */
export function searchSources(query: string): GallerySource[] {
  const q = query.toLowerCase().trim()
  if (!q) return GALLERY_SOURCES
  return GALLERY_SOURCES.filter(
    (s) =>
      s.name.toLowerCase().includes(q) ||
      s.provider.toLowerCase().includes(q) ||
      s.tagline.toLowerCase().includes(q) ||
      s.description.toLowerCase().includes(q)
  )
}

/**
 * Get sources sorted by popularity.
 */
export function getPopularSources(limit?: number): GallerySource[] {
  const sorted = [...GALLERY_SOURCES].sort((a, b) => b.popularity - a.popularity)
  return limit ? sorted.slice(0, limit) : sorted
}

/**
 * Get source count by category.
 */
export function getSourceCountByCategory(): Record<GalleryCategory, number> {
  const counts: Record<GalleryCategory, number> = {
    all: GALLERY_SOURCES.length,
    productivity: 0,
    development: 0,
    communication: 0,
    'ai-search': 0,
    storage: 0,
    analytics: 0,
    automation: 0,
    design: 0,
    finance: 0,
    ecommerce: 0,
    social: 0,
    crm: 0,
    security: 0,
    media: 0,
  }

  for (const source of GALLERY_SOURCES) {
    counts[source.category]++
  }

  return counts
}
